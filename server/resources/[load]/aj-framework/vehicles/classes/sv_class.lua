local function Notify(src, message)
    TriggerClientEvent('AJFW:Notify', src, message, 'error', 500)
end

local function customRandom(min, max)
    return min + math.random() * (max - min)
end

AJFW.Commands.Add('classes', 'AddVehicleClass', 
{
    {name="class", help="D, C, B, A, S, S+, X1, X2, X3, X4"},
    {name="tier", help="T, M, B"},
    {name="Random", help="true / false"}
}, true, function(source, args)
    local src = source

    local class = args[2]
    local tier = args[3]
    local random = args[1]

    if random == 'true' then
        random = true
    elseif random == 'false' then
        random = false
    end

    if object[class] then
        if object[class][tier] then
            if random then
                local obj = object[class][tier][1]
                local data = {}
                for k,v in pairs(obj) do
                    if type(v) == 'table' and v.min then
                        data[k] = customRandom(v.min, v.max)
                    else
                        data[k] = v
                    end
                end
                TriggerClientEvent('aj-handling:client:ReceiveData', src, data)
            else
                local obj = object[class][tier][2]
                for k,v in pairs(obj) do
                    if type(v) == 'table' and v.min then
                        data[k] = customRandom(v.min, v.max)
                    else
                        data[k] = v
                    end
                end
            end
        else
            Notify(src, "Invalid Tier")
        end
    else
        Notify(src, 'Invalid class')
    end
end,'dev')